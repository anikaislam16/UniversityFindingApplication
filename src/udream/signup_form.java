    private void signupbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupbtnActionPerformed
        if (!pass_label.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Password must be 8 character long");
        } else if (pass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fillup Password field");
        } else if (email.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fillup Email field");
        } else if (!email.getText().contains("@") || !email.getText().endsWith(".com") || email.getText().contains(" ")) {
            JOptionPane.showMessageDialog(null, "Please Give a valid Email address");
        } else if (phone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fillup Phone Number field");
        } else if (firstname.getText().isEmpty() && lastname.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Give a Name of your Account");
        } else {
            try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/project", "root", "16ani");
                PreparedStatement st1 = con.prepareStatement("Select Email From sign_up where Email = ?");
                st1.setString(1, email.getText());
                ResultSet r = st1.executeQuery();
                if (r.next()) {
                    JOptionPane.showMessageDialog(null, "This Email has been used already");
                    email.setText(null);

                } else {
                    PreparedStatement st = con.prepareStatement("insert into sign_up(First_name,Last_name,Phone_number,Email,Password) values(?,?,?,?,?)");
                    st.setString(1, firstname.getText());
                    st.setString(2, lastname.getText());
                    st.setString(3, phone.getText());
                    st.setString(4, email.getText());
                    st.setString(5, pass.getText());
                    st.executeUpdate();
                    st.close();
                    dispose();
                    login_form login = new login_form();
                    login.setVisible(true);
                }
                st1.close();
                con.close();

            } catch (SQLException ex) {
                Logger.getLogger(signup_form.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_signupbtnActionPerformed

